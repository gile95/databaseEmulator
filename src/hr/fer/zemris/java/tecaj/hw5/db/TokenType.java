package hr.fer.zemris.java.tecaj.hw5.db;

/**
 * Represents types of tokens.
 * @author Mislav Gillinger
 * @version 1.0
 */
public enum TokenType {
	/** Less than operator. */
	LESS,
	/** Greater than operator. */
	GREATER, 
	/** Less equals operator. */
	LESS_EQUALS, 
	/** Greater equals operator. */
	GREATER_EQUALS, 
	/** Equals operator. */
	EQUALS, 
	/** Different than operator. */
	DIFFERENT, 
	/** Like operator. */
	LIKE;
}
